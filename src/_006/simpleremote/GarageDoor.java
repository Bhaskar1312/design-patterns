package _006.simpleremote;

public class GarageDoor {
    public void up() {
        System.out.println("Garage door up");
    }

    public void down() {
        System.out.println("Garage door down");
    }

    public void stop() {
        System.out.println("Garage stopped?");
    }

    public void lightsOn() {
        System.out.println("Garage lights on");
    }

    public void lightsOff() {
        System.out.println("Garage lights off");
    }
}
